int	ft_str_is_alpha(char *str)
{
	int	p;

	p = 0;
	if (str[0] == '\0')
	{
		return (1);
	}
	while (str[p] != '\0')
	{
		if (str[p] <= 64 || str[p] >= 123)
			return (0);
		if (str[p] >= 91 && str[p] <= 96)
			return (0);
		p++;
	}
	return (1);
}
