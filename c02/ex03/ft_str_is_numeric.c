int	ft_str_is_numeric(char *str)
{
	int	p;

	p = 0;
	if (str[0] == '\0')
	{
		return (1);
	}
	while (str[p] != '\0')
	{
		if (str[p] < 48 || str[p] > 57)
			return (0);
		p++;
	}
	return (1);
}
