#include <unistd.h>

void	ft_swap(int *a, int *b)
{
	int	z;

	z = *a;
	*a = *b;
	*b = z;
}

int	main(void)
{
	int	a;
	int	b;

	a = 53;
	b = 57;

	ft_swap(&a, &b);
	write(1, &a, 2);
	write(1, "\n", 1);
	write(1, &b, 2);
}
